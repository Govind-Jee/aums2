package com.aums.models;



public class Department {
    private String department_id;
    private String department_name;
    private String campus_location;

    public String getDepartment_id() { return department_id; }

    public void setDepartment_id(String department_id) { this.department_id = department_id; }

    public String getDepartment_name() { return department_name; }

    public void setDepartment_name(String department_name) { this.department_name = department_name; }

    public String getCampus_location() { return campus_location; }

    public void setCampus_location(String campus_location) { this.campus_location = campus_location; }
}
