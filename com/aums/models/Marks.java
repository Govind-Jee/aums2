package com.aums.models;



public class Marks {
    private String regno;
    private String subject_id;
    private double CT1_marks;
    private double CT2_marks;

    public String getRegno() { return regno; }

    public void setRegno(String regno) { this.regno = regno; }

    public String getSubject_id() { return subject_id; }

    public void setSubject_id(String subject_id) { this.subject_id = subject_id; }

    public double getCT1_marks() { return CT1_marks; }

    public void setCT1_marks(double CT1_marks) { this.CT1_marks = CT1_marks; }

    public double getCT2_marks() { return CT2_marks; }

    public void setCT2_marks(double CT2_marks) { this.CT2_marks = CT2_marks; }
}
