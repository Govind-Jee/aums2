package com.aums.models;



public class AdminDetails {
    private String user_id;
    private String designation;
    private String admin_name;
    private String phone_number;
    private String personal_email;
    private String srm_email;

    public String getUser_id() { return user_id; }

    public void setUser_id(String user_id) { this.user_id = user_id; }

    public String getDesignation() { return designation; }

    public void setDesignation(String designation) { this.designation = designation; }

    public String getAdmin_name() { return admin_name; }

    public void setAdmin_name(String admin_name) { this.admin_name = admin_name; }

    public String getPhone_number() { return phone_number; }

    public void setPhone_number(String phone_number) { this.phone_number = phone_number; }

    public String getPersonal_email() { return personal_email; }

    public void setPersonal_email(String personal_email) { this.personal_email = personal_email; }

    public String getSrm_email() { return srm_email; }

    public void setSrm_email(String srm_email) { this.srm_email = srm_email; }
}
